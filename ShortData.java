package mypackage;

public class ShortData {
    public static void main(String[] args) {
        short a=11000;
        short b=5000;
        short c;
        System.out.println("c="+(a+b));
        System.out.println("c="+(a-b));
        System.out.println("c="+(a*b));
        System.out.println("c="+(a/b));
        System.out.println("c="+(++a));
        System.out.println("c="+(--b));
    }
}
